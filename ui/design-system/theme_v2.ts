// Theme utilities
export const getThemeValue = (theme: Theme, path: string): any => {
  const keys = path.split('.');
  let current: any = theme;
  
  for (const key of keys) {
    if (current[key] === undefined) {
      console.warn(`Theme path "${path}" not found`);
      return null;
    }
    current = current[key];
  }
  
  return current;
};

export const createThemeVariant = (baseTheme: Theme, overrides: Partial<Theme>): Theme => {
  return {
    ...baseTheme,
    ...overrides,
    colors: {
      ...baseTheme.colors,
      ...overrides.colors,
    },
    typography: {
      ...baseTheme.typography,
      ...overrides.typography,
    },
  };
};

